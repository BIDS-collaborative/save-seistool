c	$Id: hglp.f,v 1.1 2001/12/21 18:39:07 lombard Exp $
      complex function hglp (w)
c
c  .....HGLP LP response
c
      complex*8 cz(7), cp(14), ck, s, t
c
c   .....Set poles and zeros.....
c
      data ck /(3.72039E+06,0.0)/
      data cz /( 0.000000E+00, -1.053000E+00),
     2         ( 0.000000E+00, +1.053000E+00),
     3         ( 0.000000E+00,  0.000000E+00),
     4         ( 0.000000E+00,  0.000000E+00),
     5         ( 0.000000E+00,  0.000000E+00),
     6         ( 0.000000E+00,  0.000000E+00),
     7         ( 0.000000E+00,  0.000000E+00)/
c
      data cp /( -0.201000E+00, 0.000000E+00),
     2         ( -0.201000E+00, 0.000000E+00),
     3         ( -0.146600E+00, 0.000000E+00),
     4         ( -0.293200E+00, 0.000000E+00),
     5         ( -0.571200E-01, 0.000000E+00),
     6         ( -0.392800E+01, 0.000000E+00),
     7         ( -0.682300E-01, 0.000000E+00),
     8         ( -0.682300E-01, 0.000000E+00),
     9         ( -0.628300E-01, 0.000000E+00),
     &         ( -0.282000E+00, 0.000000E+00),
     &         ( -0.137200E+00,-0.591900E+00),
     &         ( -0.137200E+00, 0.591900E+00),
     &         ( -0.331200E+00,-0.241000E+00),
     &         ( -0.331200E+00, 0.241000E+00)/
c
      s = cmplx(0.,w)
c
      t = ck
c
c     loop over common zeros and poles
c
      do ii = 1, 7
        t = t * ((s - cz(ii))/(s - cp(ii)))
      end do
c
c     loop over remaining poles
c
      do ii = 8, 14
        t = t / (s - cp(ii))
      end do
c
      hglp = t
c
      return
      end
